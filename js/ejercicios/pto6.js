let numero1 = parseInt(prompt("Ingrese un primer numero"))
let numero2 = parseInt(prompt("Ingrese un segundo numero"))
if (numero1 > numero2){
    document.write ("El primer numero es mayor, el numero ingresado es: "+ numero1)
} else if (numero1 < numero2) {
    document.write ("El segundo numero es mayor, el numero ingresado es: "+ numero2)
}